export const getTextbooks = (subjectName: string, cls: number): Textbook[] => {
  const bySubject = TEXTBOOKS[subjectName];
  if (bySubject && bySubject[cls]) return bySubject[cls];
  // Проверяем смежные ключи (например «Алгебра» и «Математика»)
  if (subjectName === "Математика" || subjectName === "Алгебра") {
    const alt = TEXTBOOKS["Алгебра"]?.[cls] || TEXTBOOKS["Математика"]?.[cls];
    if (alt) return alt;
  }
  return DEFAULT_TEXTBOOKS(subjectName, cls);
};
